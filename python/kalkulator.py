def dodawanie(pierwszza, druga):
    return pierwszza + druga

def odejmowanie(pierwsza, druga):
    return pierwsza - druga

def mnozenie(pierwsza, druga):
    return pierwsza * druga

def dzielenie(pierwsza, druga):
    return pierwsza / druga


print(dodawanie(3,2),'\n',mnozenie(3,2),'\n',odejmowanie(10,5),'\n', dzielenie(10,5) )